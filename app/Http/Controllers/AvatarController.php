     */
    public function store(Request $request)
    {
        $request->validate([
        'avatar' => 'required|mimes:jpeg',
     ]);
        //return $request->all();
        $user = auth()->user();
        $user->addMedia($request->avatar)->toMediaCollection('avatar');
        return redirect()->back();

